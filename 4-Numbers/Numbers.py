number_1 = 10
number_2 = 3

print('Addition:', number_1 + number_2)
print('Subtraction:', number_1 - number_2)
print('Multiplication:', number_1 * number_2)
print('Division:', number_1 / number_2)
print('Division (Whole number):', number_1 // number_2)
print('Modulus (Remiander):', number_1 % number_2)
print('Exponent (Power):', number_1 ** number_2)

# Augmented assignment operator
x = 10
x = x + 3
print('Non augmented:', x)

# The above can be expressed simpler via the augmented assignment operator
x += 3
print('Augmented:', x)
